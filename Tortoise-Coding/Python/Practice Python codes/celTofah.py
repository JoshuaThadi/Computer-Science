# program to convert temperature in celsus to fahrenheit

celsius = 37.5
# calculate fahrenheit
fahrenheit = (celsius * 1.8) + 32
print('%0.1f degree celsius is equal to %0.1f degree fahrenheit' %(celsius, fahrenheit))

fahrenheit_f = 99.5
# calculate celsius
celsius_c = (fahrenheit_f - 32) / 1.8
print('\n%0.1f degree fahrenheit is equal to %0.1f degree celsius' %(fahrenheit_f, celsius_c))

